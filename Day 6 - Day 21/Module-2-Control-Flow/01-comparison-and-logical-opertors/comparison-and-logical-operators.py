age = 10

print(age < 0)                  # False

print(age > 0)                  # True

print(age == 0)                 # False

print(age <= 12)                # True

print(age >= 12)                # False

print(age == 18)                # False

print(age != 25)                # True

print(age > 12 and age < 18 and age != 15)    # False

print(age >= 19 and age <= 59)  # False

print(age >= 60 or age <= 100)  # True

print(not (age < 0))            # True

